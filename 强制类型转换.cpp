/*
	计算1 + 1/2 + 1/3 + ... + 1/100的值 
*/ 
#include<stdio.h>
int main()
{
	int i;
	float sum = 0;
	for(i=1;i<101;++i)
	{
		sum = sum + 1.0/i;//推荐使用此方法，将1.0/i转换为浮点类型 
		//sum = sum + (float)(1/i);这样写是不对的，
		//因为1是整型，i是整型，1/i的结果还是整型，精度丢失了 
		//也可以写成：sum = sum + 1/(float)(i);
		//之所以不在第七行就将i定义为float类型
		//是因为将float类型再转换为int时会造成精度丢失 
	}
	printf("sum=%f\n",sum); 
	return 0;
 } 
 /*
 	在Dev-C++5.11中的输出结果为： 
	----------------------
	sum=5.187378
	----------------------
*/
