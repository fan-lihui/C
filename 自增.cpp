#include<stdio.h>
int main()
{
	int i;
	int j;
	int k;
	int m;
	i = j = 3;
	k = ++i;
	m = j++;
	printf("i = %d,j = %d,k = %d,m = %d",i,j,k,m); //4,4,4,3
	return 0;
 } 
 /*
 	在Dev-C++5.11中的输出结果为：
--------------------------------------
i = 4,j = 4,k = 4,m = 3
-------------------------------------- 
	结论：
		前自增整体表达式的值是i加1之后的值（先加一再运算）
		后自增整体表达式的值是i加1之前的值（先运算，再加一） 
 */
 
